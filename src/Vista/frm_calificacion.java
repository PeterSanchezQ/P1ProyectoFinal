    private void tx_n1KeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tx_n1KeyTyped
        // solo aceptar numeros con punto
        int key = evt.getKeyChar();

        if (key >= 48 && key <= 57 || key == 46) {
            if (key == 46)//limitar ingreso a un solo punto
            {
                String dato = tx_n1.getText();
                int tama = dato.length();
                for (int i = 0; i <= tama; i++) {
                    if (dato.contains(".")) {
                        evt.setKeyChar((char) java.awt.event.KeyEvent.VK_CLEAR);
                    }

                }
            }
        } else {
            evt.setKeyChar((char) java.awt.event.KeyEvent.VK_CLEAR);
            evt.consume();
        }
    }//GEN-LAST:event_tx_n1KeyTyped

