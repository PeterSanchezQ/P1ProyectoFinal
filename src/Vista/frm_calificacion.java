    //lista de calificaciones
    public void actualizar_lista() {
        mod_Conexion modc = new mod_Conexion();
        Connection con = null;
        PreparedStatement ps = null;
        ResultSet rs = null;
        con = modc.getConexion();
        //consulta
        String sql = "select Codigo,Nombre,Apellido,Materia,Nota1,Nota2,Nota3,Notap1,Notap2,Notap3,NotaFinal,Calificacion  from calificacion";
        try {

            //consulta de insercion de datos
            ps = con.prepareStatement(sql);
            //ejecutar consulta
            rs = ps.executeQuery();
            while (rs.next()) {
                dtm.addRow(new Object[]{
                    rs.getString("Codigo"),
                    rs.getString("Nombre"),
                    rs.getString("Apellido"),
                    rs.getString("Materia"),
                    rs.getString("Nota1"),
                    rs.getString("Nota2"),
                    rs.getString("Nota3"),
                    rs.getString("Notap1"),
                    rs.getString("Notap2"),
                    rs.getString("Notap3"),
                    rs.getString("NotaFinal"),
                    rs.getString("Calificacion")
                });//fin nuevo objeto  
            }
            //cerrar base de datos
            con.close();
        }//fin try
        catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error de eliminacion: " + e);
        }//fin catch
    }
    //////////////////////////////////////////////////////////////////////////////////////////
    //limpiar lista

    public void lim_list() {
        int cont = tbl_lista.getRowCount();
        while (cont >= 1) {
            cont--;
            dtm.removeRow(cont);
        }

    }

